Marks = int(input("Enter your marks : "))

if Marks >= 90:
    print("You got an A grade.")
elif Marks >= 80:
    print("You got a B grade.")
elif Marks >= 70:
    print("You got a C grade.")
elif Marks >= 60:
    print("You got a D grade.")
elif Marks >= 50:
    print("You got an E grade.")
else:
    print("You got an F grade.")